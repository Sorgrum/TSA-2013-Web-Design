                <p><span class="highlight">HTML</span> is the basic element of all web pages. Your browser is simply a tool for reading the code on the HTML file.
				But how do you write this code to create a specific webpage?
				Time to get a little deeper.</p>
				<br />
				<h3 class="f3">Let&#39;s take another by look at that sample website.</h3>
				<br />
				<p>On the second line, there is the tag: </p><pre class="code codesnip">  <span class="angopen">&lt;</span><span class="tag">html</span><span class="angclose">&gt;</span></pre>
				<br />
                <br />
                <p>A <span class="color1">tag</span> is placed to indicate an element of the webpage and the name of the tag is always between these guys &lt;&gt;.</p>
				<p>The tag <code class="code"><span class="angopen">&lt;</span><span class="tag">html</span><span class="angclose">&gt;</span></code> for instance, initiates the HTML file. Every website has this tag at the beginning of their code. </p>
				<p>At the bottom of the code, there is the tag <code class="code codesnip2">  <span class="angopen">&lt;</span><span class="tag">/html</span><span class="angclose">&gt;</span></code>.&nbsp;It indicates the end of the web page.</p>
                <br />
                <p>All tags operate in this way, with an <strong class="color3">opening and closing</strong> tag.</p>
				<p>Let&#39;s look at some of the other tags on the sample page.</p>
                <br />
                <br />
				<p>The <code class="code"><span class="angopen">&lt;</span><span class="tag">head</span><span class="angclose">&gt;</span></code> tag is used to place global info about your site. This &#34;global info&#34;, is simply important information and descriptions about your site that are used by browsers. For example, in your <code class="code"><span class="angopen">&lt;</span><span class="tag">head</span><span class="angclose">&gt;</span></code> tag, you might place</p>
				<p>the <code class="code"><span class="angopen">&lt;</span><span class="tag">title</span><span class="angclose">&gt;</span></code> tag! This is used to <span class="color1">name</span> the page. Since we put &#34;Sample Page&#34; in between the initiating tag <code class="code"><span class="angopen">&lt;</span><span class="tag">title</span><span class="angclose">&gt;</span></code> and the ending tag <code class="code"><span class="angopen">&lt;</span><span class="tag">/title</span><span class="angclose">&gt;</span></code>, the name of the page will be changed to &#34;Sample Page&#34;. It&#39;s simple as that! The title of the page is displayed on the tab heading on your browser and will be seen when your website is indexed by a search engine.</p>
