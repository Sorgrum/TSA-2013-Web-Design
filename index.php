                <h2 class="csstitle">CSS</h2>
                <br />
                <p>&nbsp;&nbsp;&nbsp;&nbsp;At this point, you might be wondering how these <span class="color1">elaborate</span>, fancy, and colorful websites you are used to seeing on the web are made. Well, that is where CSS comes in.</p>
                <p><span class="color1">CSS</span> stands for <span class="color1">C</span>ascading <span class="color1">S</span>tyle <span class="color1">S</span>heets. In other words, the code that makes your website look nice. It does this by allowing you to manipulate formatting and style.</p>
                <p>After all, what&#39;s the point of website if it doesn&#39;t have style?</p>
                <br />
                <p class="reminder"><span class="reminderhead">Reminder:</span> CSS code can be written on a file separate from your main HTML file for organization, but for the sake of simplicity, let&#39;s focus on inline CSS.</p>
                <br />

                <div class="cssexample">
                    <img src="img/cssexample.png" class="example" alt="An example of website with CSS" />
                </div>

                <p>Here is a sample <span class="color1">webpage</span> that uses CSS. It certainly has more life to it than the previous site, doesn&#39;t it?</p>
                <p>There are many ways you can use CSS to <span class="color1">design your  website</span>. Let&#39;s go over some of the basic functions.</p>
                <p>You might <span class="color1">notice</span> that different blocks of text in the sample page have different <span class="color1">fonts, sizes and colors</span>. You can edit font size (along with many other things) using the &#39;style&#34; attribute.</p>
                <br />
                <p>While you initiate your paragraph with the <code class="code"><span class="angopen">&lt;</span><span class="tag">p</span><span class="angclose">&gt;</span></code> tag, you can <span class="color1">edit the style</span> of everything in that paragraph.</p>
                <p>A paragraph that starts as so: <code class="code"><span class="angopen">&lt;</span><span class="tag">p</span> <span class="angopen">style=&#39;</span><span class="style-color">font-size</span><span class="angclose">:</span> <span class="value-color">50px</span><span class="angclose">&#39;&gt;</span></code>, will contain text with a style that equals the font size of 50px. See how the <span class="color1">style attribute</span> works? If you want to reduce the font size, you can simply change the number 50 to a smaller number.</p>
                <br />
                <br />
                <p class="reminder"><span class="reminderhead">Reminder:</span> Unfortunately, you will have to memorize the exact syntax of the style attribute. But once you do, you will be able to manipulate your text with impressive detail.</p>
                <br />
                <br />
                <p>If you want to <span class="color1">change</span> a paragraphs font color, your style line will look like this: <code class="code"><span class="angopen">&lt;</span><span class="tag">p</span> <span class="angopen">style=&#39;</span><span class="style-color">color</span><span class="angclose">:</span> <span class="value-color">red</span><span class="angclose">&#39;&gt;</span></code>.</p>
                <p>Noticing a trend? Once you initiate the <span class="color1">style attribute</span>, you add an equals sign, state which attribute you want to edit, and then state the specific change.</p>
                <p>Want to change your <span class="color1">font</span>? Try using the font-family attribute as so: <code class="code"><span class="angopen">&lt;</span><span class="tag">p</span> <span class="angopen">style=&#39;</span><span class="style-color">font-family</span><span class="angclose">:</span> <span class="value-color">Impact</span><span class="angclose">&#39;&gt;</span></code>.</p>
                <br />
                <br />
                <p class="reminder"><span class="reminderhead">Reminder:</span> The style attribute isn&#39;t limited to editing paragraphs. You use it to edit any text, for instance, your heading.</p>
                <br />
                <br />
                <br />
                <br />
                <p class="highlight">So your text is all fancy looking. What about the rest of the website?</p>
                <p>Inline CSS isn&#39;t only used to manipulate text.. You can use it to <span class="color1">change the look</span> of your website as a whole.</p>
                <p>For instance, let&#39;s say you want to change your background color to green. Since the entire <span class="color1">website&#39;s content</span> is encompassed inside the body tag, you should edit the body tag.</p>
                <p>Your code might look like this: <code class="code"><span class="angopen">&lt;</span><span class="tag">p</span> <span class="angopen">style=&#39;</span><span class="style-color">background-color</span><span class="angclose">:</span> <span class="value-color">green</span><span class="angclose">&#39;&gt;</span></code></p>
                <br />
                <p> There are a <span class="color1">plethora</span> of other CSS functions you might be working with in the future. They can turn an ordinary site into an <span class="color1">extraordinary site</span>.</p>
